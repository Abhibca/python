#defing String

my_string = 'Hello'
print(my_string)
print(type(my_string))

my_string = "Hello"
print(my_string)
print(type(my_string))

my_string = '''Hello'''
print(my_string)
print(type(my_string))

#triple quotes string can extend multiple lines
my_string=""""Hello Welcome to the 
                    world of python"""
print(my_string)
print(type(my_string))

#using * operator we can print multiple times
my_string = 'hello' * 15
print(my_string)

#using + operator we can concate the string
my_string = 'a' + 'a' + 'Welcome to rajkot'
print(my_string)

#using - operator we can use it gives error
my_string = 'a'-'a'
print(my_string)

#using + operator with digit it gives error
my_string = 'a' + 2
print(my_string)


