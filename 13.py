# Accing String Character in python

str = 'programing'
print('str = ',str)

###first character
print('str[0] = ',str[0])

#last charcter using nagetive index
print('str[-1] = ',str[-1])

#Slicing 2nd to 5th charcter
print('str[1:5] = ',str[1:5])

#slicing 6th to 2nd last character
print('str[5:-2] = ',str[5:-2])

